package finalassignment;

public class Trianee {

	private String trainee1;
	private String trainee2;
	private String trainee3;

	public Trianee(String trainee1, String trainee2, String trainee3) {
		super();
		this.trainee1 = trainee1;
		this.trainee2 = trainee2;
		this.trainee3 = trainee3;
	}

	public String getTrainee1() {
		return trainee1;
	}

	public void setTrainee1(String trainee1) {
		this.trainee1 = trainee1;
	}

	public String getTrainee2() {
		return trainee2;
	}

	public void setTrainee2(String trainee2) {
		this.trainee2 = trainee2;
	}

	public String getTrainee3() {
		return trainee3;
	}

}
